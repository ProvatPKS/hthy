package com.home.app.model;

public class JobBotUserTokenRequest {

    private String jbotUserName;
    private String jbotToken;

    public String getJbotUserName() {
        return jbotUserName;
    }

    public void setJbotUserName(String jbotUserName) {
        this.jbotUserName = jbotUserName;
    }

    public String getJbotToken() {
        return jbotToken;
    }

    public void setJbotToken(String jbotToken) {
        this.jbotToken = jbotToken;
    }
}
